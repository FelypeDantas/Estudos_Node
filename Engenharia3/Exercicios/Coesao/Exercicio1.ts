class Calculadora {
    somar(a: number, b: number): number {
      return a + b;
    }
  
    subtrair(a: number, b: number): number {
      return a - b;
    }
  
    multiplicar(a: number, b: number): number {
      return a * b;
    }
  
    dividir(a: number, b: number): number {
      return a / b;
    }
  }